# Welcome the player to the game and state the mission
print("Welcome to Treasure Island.")
print("Your mission is to find the treasure.")

# Ask the player to choose a direction at a crossroad
choice1 = input('You\'re at a crossroad, where do you want to go? '
                'Type "left" or "right".\n').lower()

# If player chooses left, proceed to the lake scenario
if choice1 == "left":
    # Ask the player whether to wait for a boat or swim across the lake
    choice2 = input('You\'ve come to a lake. '
                    'There is an island in the middle of the lake. '
                    'Type "wait" to wait for a boat. '
                    'Type "swim" to swim across.\n').lower()
    if choice2 == "wait":
        # Upon waiting safely, ask the player to choose one of three doors
        choice3 = input("You arrive at the island unharmed. "
                        "There is house with 3 doors. One red, "
                        "one yellow and one blue. "
                        "Which colour do you choose?\n").lower()
        # Check door choice outcomes
        if choice3 == "red":
            print("It's a room full of fire. Game Over")
        elif choice3 == "yellow":
            print("You found the treasure. You Win!")
        elif choice3 == "blue":
            print("You enter a room of beasts. Game Over.")
        else:
            print("You chose a door that doesn't exist. Game Over.")
    else:
        # If player tries to swim, they get attacked by a trout
        print("You got attacked by an angry trout. Game Over.")

# If player chooses right at the crossroad, they fall in a hole
else:
    print("You fell in to a hole. Game Over.")
